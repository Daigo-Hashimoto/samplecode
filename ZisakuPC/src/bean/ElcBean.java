package bean;

public class ElcBean {
	private String elcModelNo;
	private String capacity;
	private String url;
	/**
	 * @return elcModelNo
	 */
	public String getElcModelNo() {
		return elcModelNo;
	}
	/**
	 * @param elcModelNo セットする elcModelNo
	 */
	public void setElcModelNo(String elcModelNo) {
		this.elcModelNo = elcModelNo;
	}
	/**
	 * @return capacity
	 */
	public String getCapacity() {
		return capacity;
	}
	/**
	 * @param capacity セットする capacity
	 */
	public void setCapacity(String capacity) {
		this.capacity = capacity;
	}
	/**
	 * @return url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * @param url セットする url
	 */
	public void setUrl(String url) {
		this.url = url;
	}

}
